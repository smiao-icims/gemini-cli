/**
 * Get the appropriate ASCII art based on auth type and terminal width
 */
export function getAsciiArt(authType: AuthType | undefined, terminalWidth: number, useAlternative = false): string {
  const isOllama = authType === AuthType.OLLAMA;
  
  if (isOllama) {
    if (useAlternative) {
      // Use the alternative Ollama-themed ASCII art
      const widthOfLongOllama2 = getAsciiArtWidth(longOllamaLogo2);
      return terminalWidth >= widthOfLongOllama2 ? longOllamaLogo2 : shortOllamaLogo2;
    } else {
      // Use the enhanced Gemini logo with Ollama branding
      const widthOfLongOllama = getAsciiArtWidth(longOllamaLogo);
      return terminalWidth >= widthOfLongOllama ? longOllamaLogo : shortOllamaLogo;
    }
  }
  
  // Default to original Gemini logos
  const widthOfLongLogo = getAsciiArtWidth(longAsciiLogo);
  return terminalWidth >= widthOfLongLogo ? longAsciiLogo : shortAsciiLogo;
}

/**
 * Get the width of ASCII art for layout calculations
 */
function getAsciiArtWidth(asciiArt: string): number {
  if (!asciiArt) {
    return 0;
  }
  const lines = asciiArt.split('\n');
  return Math.max(...lines.map(line => line.length));
}
